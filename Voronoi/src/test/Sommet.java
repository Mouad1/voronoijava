package test;
// Un sommet, c'est trois float
public class Sommet {
	protected float X,Y,Z;

	public Sommet(float x, float y, float z) {
		super();
		X = x;
		Y = y;
		Z = z;
	}

	public float getX() {
		return X;
	}

	public float getY() {
		return Y;
	}

	public float getZ() {
		return Z;
	} 
	
	

}
